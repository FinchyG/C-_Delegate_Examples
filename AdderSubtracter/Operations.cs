using System;
using System.Collections.Generic;
using System.Text;

namespace AdderSubtracter
{
    class Operations
    {
        public static void Adder(int x, int y)
        {
            Console.WriteLine("The sum of the two numbers is {0}", x + y);
        }

        public static void Subtracter(int x, int y)
        {
            Console.WriteLine("The difference of the two numbers is {0}", x - y);
        }
    }
}
